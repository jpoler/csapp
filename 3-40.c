#include <stdio.h>

#define N 16

typedef int fix_matrix[N][N];

void fix_set_diag(fix_matrix A, int val) {
    int *Astart = &A[0][0];
    int *Aend = &A[N][N];
    do {
        *Astart = val;
        Astart += N+1;
    } while (Astart != Aend);
}

void print_fix_matrix(fix_matrix A) {
    int i, j;
    for (i = 0; i < N; i++) {
        for (j = 0; j < N; j++) {
            printf("%d ", A[i][j]);
        }
        printf("\n");
    }
}

